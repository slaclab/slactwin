    def create_archive(self, machine_name):
        """Create an archive HDF5 from current PV values for a selected machine.

        Args:
            machine_name (str): machine to archive, ex. cu_inj, sc_inj
        """

        def _create_hdf5(values):
            values.timestamp = datetime.now().isoformat()
            fn = (
                machine_name
                + "-"
                + re.sub(r"\..*", "", values.timestamp).replace(":", "_")
                + ".h5"
            )
            with h5py.File(fn, "w") as f:
                g = f.create_group(machine_name)
                for n in values:
                    if isinstance(values[n], numpy.ndarray):
                        g.create_dataset(n, data=values[n])
                    else:
                        g.attrs[n] = values[n]
            return f"created PV archive: {fn}"

        def _init():
            v = PKDict()
            for n in self.PVNAMES[machine_name]:
                v[n] = None
                epics.PV(n, auto_monitor=epics.dbr.DBE_VALUE).add_callback(
                    partial(_monitor_callback, v)
                )
            return v

        def _monitor_callback(values, pvname, value, **kwargs):
            pkdc(f"{pvname}: {value}")
            if pvname not in values:
                raise AssertionError(f"Callback on unknown pvname: {pvname}")
            if isinstance(value, numpy.ndarray) and not len(value):
                return
            values[pvname] = value

        _MAX_TRIES = 5
        t = 0
        values = _init()
        while t < _MAX_TRIES:
            t += 1
            time.sleep(1)
            if any([v is None for v in values.values()]):
                pkdlog(
                    "retrying, no values received for pvs: {}",
                    [n for n, v in values.items() if v is None],
                )
                continue
            return _create_hdf5(values)
        raise AssertionError("Exceeded connection attempts")
